"""Node modules cleanup module."""

import subprocess
from collections.abc import AsyncIterator
from pathlib import Path

from macsweep.modules.base import CleanupItem, CleanupModule


class NodeModulesModule(CleanupModule):
    """Find and clean node_modules directories."""

    name = "Node Modules"
    description = "node_modules directories from JavaScript/TypeScript projects"
    category = "Developer"
    safe_by_default = True

    def __init__(self, scan_paths: list[Path] | None = None) -> None:
        self.scan_paths = scan_paths or [Path.home() / "www"]

    async def scan(self) -> AsyncIterator[CleanupItem]:
        """Scan for node_modules directories."""
        for base_path in self.scan_paths:
            if not base_path.exists():
                continue

            # Use find with -prune to avoid descending into node_modules
            try:
                result = subprocess.run(
                    ["find", str(base_path), "-type", "d", "-name", "node_modules", "-prune"],
                    capture_output=True,
                    text=True,
                    timeout=120,
                )
                paths = [p.strip() for p in result.stdout.strip().split("\n") if p.strip()]
            except (subprocess.TimeoutExpired, subprocess.SubprocessError):
                continue

            for path_str in paths:
                path = Path(path_str)
                if not path.exists():
                    continue

                size = self._get_size_fast(path)
                if size > 0:
                    # Get project name from parent directory
                    project = path.parent.name
                    rel_path = str(path).replace(str(Path.home()), "~")

                    yield CleanupItem(
                        path=path,
                        size=size,
                        category=self.category,
                        subcategory="node_modules",
                        description=f"{project}: {rel_path}",
                        safe_to_delete=True,
                        requires_confirmation=False,
                    )

    def _get_size_fast(self, path: Path) -> int:
        """Get directory size using du for speed."""
        try:
            result = subprocess.run(
                ["du", "-sk", str(path)],
                capture_output=True,
                text=True,
                timeout=30,
            )
            if result.returncode == 0:
                # du -sk returns size in KB
                size_kb = int(result.stdout.split()[0])
                return size_kb * 1024
        except (subprocess.TimeoutExpired, subprocess.SubprocessError, ValueError, IndexError):
            pass
        return 0
